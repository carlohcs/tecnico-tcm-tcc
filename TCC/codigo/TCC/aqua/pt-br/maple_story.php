					<p><img src="img/maple.png" alt="Maple Story"/>
					<strong>MapleStory</strong> é um MMORPG (Massive Multiplayer Online Role-Play Game) criado em maio de 2005 pela 
					Nexon e programado pela Wizet. O jogo possui gráficos bidimensionais e personagens desenhados ao estilo super deformed.
					Diferentemente da maioria dos MMORPGs atuais, Maple Story tem um sistema de batalhas, no qual o jogador comanda 
					os movimentos dos personagens com o teclado.<br/><br/>
					O jogo possui um sistema de Classes/Jobs, que é quase igual na grande maioria dos jogos. Atualmente possui mais de 
					2.000.000 de contas de jogadores e pelo menos 7.000.000 de personagens criados. Cada conta ou jogador possui um nome 
					único que não pode ser utilizado por outro jogador, o que garante que o seu personagem não será confundido com outro.<br/><br/>
					
					
					<strong>Ilhas do jogo</strong><br/><br/>
					
					O jogo se inicia na Ilha Maple, na Cidade dos Cogumelos – onde se encontra o campo de treinamento. Nesta ilha existem 
					ainda<img src="img/maple2.png" class="maple" alt="Maple Story"/> as cidades de Amherst – a principal cidade da Ilha Maple, em que é possível encontrar diversas criaturas 
					relativamente fáceis de serem batidas, ótimo para qualquer iniciante, e do Porto Sul – última cidade e saída da 
					Ilha Maple para outra ilha, a Ilha Victoria.<br/><br/>

					Quando você alcança o sétimo nível, você pode ir para a Ilha Victoria, que é onde o jogo se desenrola de fato. 
					Aqui existem sete cidades onde os jogadores poderão encontrar de tudo. A primeira cidade da Ilha é Porto Lith, onde podem 
					ser encontradas diversas missões para os iniciantes. Aqui se encontram as cidades onde os jogadores decidirão qual classe
					desejam seguir.<br/><br/>

					A outra ilha de Maple Story se chama Ilha Ossyria, que com uma única cidade, Orbis, possui o principal aeroporto do jogo. 
					É também nesta cidade onde você e seus amigos poderão criar seus clãs.

